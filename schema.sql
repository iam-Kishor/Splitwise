-- 3. ROW LEVEL SECURITY (RLS) POLICIES
---------------------------------------------------------

-- Enable RLS on all tables
ALTER TABLE public.users ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.groups ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.group_members ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.expenses ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.expense_splits ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.settlements ENABLE ROW LEVEL SECURITY;

-- 3.1 Users Policies
DROP POLICY IF EXISTS "Allow anyone authenticated to view profiles" ON public.users;
CREATE POLICY "Allow anyone authenticated to view profiles"
  ON public.users FOR SELECT TO authenticated
  USING (true);

DROP POLICY IF EXISTS "Allow users to insert their own profile" ON public.users;
CREATE POLICY "Allow users to insert their own profile"
  ON public.users FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = id);

DROP POLICY IF EXISTS "Allow users to update their own profile" ON public.users;
CREATE POLICY "Allow users to update their own profile"
  ON public.users FOR UPDATE TO authenticated
  USING (auth.uid() = id)
  WITH CHECK (auth.uid() = id);

-- 3.2 Groups Policies
DROP POLICY IF EXISTS "Allow group members to view groups" ON public.groups;
CREATE POLICY "Allow group members to view groups"
  ON public.groups FOR SELECT TO authenticated
  USING (
    id IN (SELECT group_id FROM public.group_members WHERE user_id = auth.uid())
  );

DROP POLICY IF EXISTS "Allow users to create groups" ON public.groups;
CREATE POLICY "Allow users to create groups"
  ON public.groups FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = created_by);

DROP POLICY IF EXISTS "Allow group members to update group details" ON public.groups;
CREATE POLICY "Allow group members to update group details"
  ON public.groups FOR UPDATE TO authenticated
  USING (
    id IN (SELECT group_id FROM public.group_members WHERE user_id = auth.uid())
  );

DROP POLICY IF EXISTS "Allow creator to delete groups" ON public.groups;
CREATE POLICY "Allow creator to delete groups"
  ON public.groups FOR DELETE TO authenticated
  USING (created_by = auth.uid());

-- 3.3 Group Members Policies
DROP POLICY IF EXISTS "Allow members to view group member lists" ON public.group_members;
CREATE POLICY "Allow members to view group member lists"
  ON public.group_members FOR SELECT TO authenticated
  USING (true);

DROP POLICY IF EXISTS "Allow members to invite or join group" ON public.group_members;
CREATE POLICY "Allow members to invite or join group"
  ON public.group_members FOR INSERT TO authenticated
  WITH CHECK (
    -- User is joining themselves
    auth.uid() = user_id 
    OR 
    -- Or user is already a member of the group
    group_id IN (SELECT gm.group_id FROM public.group_members gm WHERE gm.user_id = auth.uid())
    OR
    -- Or user is the creator of the group
    group_id IN (SELECT g.id FROM public.groups g WHERE g.created_by = auth.uid())
  );

DROP POLICY IF EXISTS "Allow members to leave or creator to remove member" ON public.group_members;
CREATE POLICY "Allow members to leave or creator to remove member"
  ON public.group_members FOR DELETE TO authenticated
  USING (
    user_id = auth.uid() 
    OR 
    group_id IN (SELECT g.id FROM public.groups g WHERE g.created_by = auth.uid())
  );

