-- SELECT POLICY FOR STORAGE
DROP POLICY IF EXISTS "Allow group members to view group receipts" ON storage.objects;
CREATE POLICY "Allow group members to view group receipts" 
ON storage.objects FOR SELECT TO authenticated 
USING (
  bucket_id = 'receipts' AND 
  public.safe_cast_uuid(split_part(name, '/', 1)) IN (
    SELECT group_id FROM public.group_members WHERE user_id = auth.uid()
  )
);

